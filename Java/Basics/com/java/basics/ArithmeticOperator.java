package com.java.basics;

/**
 * ArithmeticOperator
 */
public class ArithmeticOperator
{

    public static void main(final String[] args)
    {
        String start, middle, end, result;
        start = "Talk is cheap. ";
        middle = "Show me the code. ";
        end = "Linus Torvalds";

        result = start + middle + end;

        System.out.println(result);
    }
}